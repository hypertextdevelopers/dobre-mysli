                        <p>Absolwentka Politechniki Opolskiej na wydziale Wychowania Fizycznego i Fizjoterapii. W swojej pracy magisterskiej szczegółowo zbadała wpływ diety na rozwój miażdżycy w organizmie. Absolwentka dietetyki w Instytucie Dietetyki i Promocji Zdrowia przy Centrum Kształcenia Ustawicznego w Katowicach. Instruktorka fitness, wiele lat aktywnie związana z tańcem i sportem. Prowadząca zajęcia z zakresu aqua aerobiku, aerobiku i dance aerobiku. Uczestniczka wielu kursów i szkoleń na terenie Polski, między innymi z zakresu dietetyki, dietetyki sportowej oraz dietoterapii. Obecnie studentka Psychodietetyki w Opolu. Założyciel gabinetu dietetycznego Food Labs, działającego na terenie województwa opolskiego.</p>
