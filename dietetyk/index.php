                        <p>W dotychczasowej pracy podejmowała współprace dotyczące zdrowego stylu życia przy projektach unijnych między innymi z Pensjonatem Seniora. Ponadto stale bierze udział w szkoleniach i konferencjach aby poszerzać swoją wiedzę.</p>
