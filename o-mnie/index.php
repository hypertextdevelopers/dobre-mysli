                    <h1>O mnie słówek kilka…</h1>
                </header>
                <section>
                    <p>Ciekawość życia, ciekawość siebie, chęć nawiązywania i bycia w relacji z innymi - to są właśnie te słowa, o których mógłbym powiedzieć, że dotyczą właśnie mnie.</p>
                    <p>A oprócz tych słów, co jeszcze mógłbym powiedzieć o sobie?</p>
                    <p>Nazywam się Waldemar Wczasek i urodziłem się w Kędzierzynie – Koźlu (woj. opolskie) w 1976 roku. Nigdy nie zmieniłem swojego miejsca zamieszkania, a swoje miasto uważam za najlepsze miejsce na świecie właśnie dla mnie. Tu się urodziłem, wzrastałem, uczyłem – poznawałem życie i ludzi.</p>
                    <p>Tak się też wydarzyło, iż tutaj poznałem i zakochałem się w mojej przyszłej żonie Ewelinie. Jesteśmy małżeństwem od 2006 roku. Razem kochamy i wychowujemy nasze dwie córeczki.</p>
                    <p>Jedną z moich głównych pasji jest psychoterapia, w której najbardziej spodobała mi się ujęcie systemowe. Swoją wiedzę i praktykę zdobywałem (i zdobywam nadal) <br>w Wielkopolskim Towarzystwie Terapii Systemowej. Od 2016 roku praktykuję jako psychoterapeuta systemowy w ujęciu prywatnym.</p>
                    <p>Kolejną moją pasją jest winiarstwo. Ta pasja zrodziła się w 2009 roku i trwa po dzień dzisiejszy. Więcej informacji na ten właśnie temat jest pod zakładką „Wino”.</p>
                    <p>Ciekawa książka, teatr, góry, dobry żart, uśmiech, wspólne rozmowy i bycie z ludźmi – to są te fragmenty mojego życia, które dopełniają jego całość i nie umiem sobie ich <br>w żaden sposób odmówić.</p>
