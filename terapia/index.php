                    <h1>Dlaczego psychoterapia systemowa? – garść informacji</h1>
                </header>
                <section>
                    <p>W pracy psychoterapeutycznej wybrałem ujęcie systemowe. Przekonał mnie w tej metodzie sposób pracy z klientami , ich relacjami i historiami życia. Przekonała mnie również otwartość tej metody na pracę z zasobami klienta. Terapia systemowa jest takim ujęciem, które współpracuje z klientem, uznaje jego świat, przeżycia, emocje. Sama terapia zorientowana jest na rozwój klienta, który przynosi zadowalający skutek dla niego samego, jego relacji, jego samopoczucia i funkcjonowania w jego świecie.</p>
                    <h2>Jak pracuję?</h2>
                    <p>Od ośmiu lat zajmuję się psychoterapią. Ciągle dbam o to, by mój proces kształcenia podlegał ciągłemu rozwojowi. Moja praca jako psychoterapeuty zaczęła się w 2016 roku. To, jak rozmawiam z klientami i jak z nimi pracuję jest stale superwizowane przez doświadczonego terapeutę (superwizora) z Wielkopolskiego Towarzystwa Terapii Systemowej. Jest to sposób monitorowania własnej pracy psychoterapeuty.</p>
                    <p>Terapia to cykliczne wspólne spotkania oparte na wspólnej rozmowie psychoterapeuty z klientem. Pracuję tak, aby klient w wyniku tych rozmów mógł pozyskać dla siebie konkretne, efektywne i skuteczne narzędzia, dzięki którym będzie umiał aktywnie poradzić sobie ze swoimi trudnościami. Dbam o przyjazny i życzliwy przebieg terapii. Istotnym elementem prowadzonych rozmów jest poczucie bezpieczeństwa mojego klienta. Pracuję w sposób bezpieczny dla klienta oraz skrupulatnie dbam o dyskrecję. Wszystkie rozmowy, treści, dane klienta podlegają całkowitej tajemnicy.</p>
                    <h2>Kogo zapraszam na rozmowy terapeutyczne?</h2>
                    <p>Prowadzę rozmowy terapeutyczne w ujęciu prywatnym. Pracuję z klientami indywidualnymi,  parami oraz rodzinami. Będą także organizowane przeze mnie propozycje terapii grupowej (ich propozycja ukazywać się będą na tej stronie internetowej). </p>
                    <p>Pracuję także w krótkoterminowej terapii lęku, zaburzeń odżywiana (anoreksja, bulimia). Oferuję także pomoc dla osób po rozstaniu czy też stracie. Osoby chcące podnieść swoje kompetencje psychospołeczne w zakresie umiejętności komunikacyjnych, wyrażania sowich emocji, stawiania granic, utrzymania osobistego dobrostanu i dobrej relacji z otoczeniem – również zapraszam na rozmowy.</p>
                    <h2>Jak się można ze mną umówić na spotkanie?</h2>
                    <p>Aby umówić się na terapię, proszę o kontakt telefoniczny pod numerem +48 509 470 232. Rozmowa ta ma na celu przedstawienie wstępnych informacji, jakiego tematu miałaby dotyczyć terapia. Następnym krokiem jest umówienie się na wizytę konsultacyjną, która u mnie jest przeprowadzana bezpłatnie. Po wizycie konsultacyjnej klient decyduje, czy oferta terapii jest dla Niego do przyjęcia.  Jeśli odpowiedź jest pozytywna, wtedy klient umawia się na kolejne spotkanie, które już będzie pierwszym spotkaniem w ujęciu terapeutycznym. Wizyta konsultacyjna ma na celu wstępne omówienie tematu terapii i ofertę współpracy z mojej strony i  osoby (lub osób) zainteresowanej  (zainteresowanych)  terapią.</p>
