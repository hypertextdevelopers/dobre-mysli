                        <h2>Odmiany na białe wino</h2>
                        <p>Dobór nasadzeń w takim trudnym terenie również okazał się nie lada sztuką. Po wielu próbach udało się wyselekcjonować odmiany, które świetnie sobie poradziły w tych warunkach i dają ciekawe, aromatyczne wina. Odmiany na białe wina to Jutrzenka, Solaris oraz Seyval Blanc. Odmiany te dają aromatycznie owocowe i świeże w smaku wina. Wina białe cieszą się zdecydowaną popularnością wśród naszych gości.</p>
