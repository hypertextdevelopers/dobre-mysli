                    <h1>Winnica nad Dwoma Stawami</h1>
                </header>
                <div class="boxes">
                    <section>
                        <p>Terroir na którym została zasadzona winnica to teren dosyć trudny do prowadzenia i uprawiania winorośli. Jest to miejsce dosyć wilgotne, leżące bardzo blisko torfowiska. Wiosną można tu doświadczyć gęstych mgieł, a co gorsza majowych przymrozków, które szczególnie zagrażają dopiero co rozwijającym się zawiązkom latorośli. Na początku w mojej opinii i opinii o wiele bardziej doświadczonych winiarzy przeważał myśl, że krucho będzie na takim terenie pozyskać dobrej jakości owoc – jest to podstawa powstania dobrej jakości wina. Jednak nie doceniłem tej cudownej rośliny – doskonale poradziła sobie w tych trudnych warunkach i dziś można spróbować smakowitych win pochodzących z naszej winnicy. Uprawa winorośli to trudne i pracochłonne zadanie – ale jeden łyk, albo dwa wina wytworzonego własnoręcznie od początku do końca  rekompensuje wszystkie poniesione nakłady i wysiłki.</p>
