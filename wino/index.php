                    <h1>Wino</h1>
                </header>
                <div class="boxes">
                    <section>
                        <p>Wino i winiarstwo stało się moją pasją w 2009 roku. Skąd pomysł, że na takiej dziwnej ziemi można zająć się winiarstwem ? Będąc na Węgrzech, w mieście Eger miałem niebywałą i w zasadzie pierwszą w moim życiu okazję dotknąć z bliska i posmakować, co oznacza kultura winiarska. Przepiękne winnice, chłodne i przepastne piwnice po brzegi wypełnione różnorakimi gatunkami win, otwartość i radość ludzi, spotykających się przy lampce wyśmienitego wina po to, by porozmawiać o wszelkich tematach życia, o winie, o troskach i radościach – w zasadzie o  wszystkim. To co zobaczyłem i doświadczyłem bardzo mnie zachwyciło, ujęło – ja po prostu w kulturze i ludziach wina się zakochałem. Po powrocie do Polski, do siebie, mając kawałek wolnej ziemi, postanowiłem zacząć uprawiać winorośl i wyrabiać ten ze wszechmiar szlachetny trunek. Chciałem choć trochę przenieść to do siebie, co widziałem w Egerze. I tak przenoszę tę kulturę wina po dzień dzisiejszy… </p>
