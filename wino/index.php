                    <section>
                        <p>To taki czas, gdy winiarz cieszy się, że dotrwał do tego czasu – a raczej dotrwały winogrona w winnicy (po drodze istnieje wiele możliwości na to, że winobranie może się nie odbyć – grad, choroby, szpaki, osy a także szpaki na nogach). Jest to moment, gdy winogrona są już dobrze wysłodzone przez promienie słoneczne – na przykład odmiana Solaris potrafi zgromadzić maksymalnie nawet około 250 – 260 gram cukru na litr moszczu (soku wyciśniętego z winogron). Oczywiście potrzebna jest duża ilość słońca przez cały okres dojrzewania owoców winorośli. Każda odmiana zbierana jest w nieco odmiennych terminach – ale zbiory przypadają najczęściej na wrzesień i początek października. Potem następuje niełatwy i pracochłonny proces produkcji wina. Ale jak już pisałem wyżej, warto – dlaczego ? Zapraszam do naszej winiarni.</p>
