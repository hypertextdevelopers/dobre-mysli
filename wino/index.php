                        <h2>Odmiany na wino czerwone</h2>
                        <p>Cabernet Cortis, Monarch  to dwie odmiany czerwonych winogron, z których wyrabiamy wino o intensywnym kolorze czerwonym i nie mniej intensywnym aromacie. Te dwie odmiany dają jedno wino o nazwie Cabernet Monarch (cuvee) – ciekawe w smaku wino o charakterystycznym aromacie czarnej porzeczki, za który to aromat szczególnie odpowiada odmiana Cabernet Cortis. Istnieje pewna opinia, że lepiej w naszych szerokościach geograficznych uprawiać raczej białe odmiany winorośli – ja się z tą opinią nie zgadzam. Uważam, że można wytworzyć z czerwonych odmian bardzo interesujące i smakowite trunki.</p>
